<h1>Site Research</h1>

<p>Due Date: Day of meeting</p>

<p>
  You will be meeting with Jamie, one of the farmers, to discuss building a
  replacement for <a href="https://northofboston.farm/"
    >https://northofboston.farm/</a
  >. Your task is to do research ahead of that meeting so that you are familiar
  with what exists today and the current perception of the farm.
</p>

<p>You are expected to investigate the following:</p>

<ul>
  <li>
    Explore the entire site, click every link, fill out every form, get an
    indepth view of what it has to offer.
  </li>
  <li>
    Place an order for delivery, make sure to note in the comments that this is
    research for the course so you don't get charged.
  </li>
  <li>Look for any articles that have been written up about them.</li>
  <li>Search for any reviews people have written.</li>
  <li>See what shows up on search engines when you search for them.</li>
  <li>Examine other sites which do something similar.</li>
</ul>

<p>
  If you can think of any other pieces of information you can gather about them,
  feel free to do those as well. Your goal is to know as much about them as
  possible going into the meeting so you can spend the time with them focused on
  those things only they can tell you.
</p>

<p>
  Every assignment will involve a Pull Requeston <a
    href="https://github.com/lackita/northofboston.farm">this github repo</a
  >. You can find general instructions on how to create a pull request
  <a
    href="https://docs.github.com/en/pull-requests/collaborating-with-pull-requests/proposing-changes-to-your-work-with-pull-requests/creating-a-pull-request"
    >here</a
  >, reach out to Colin if you have trouble getting that submitted. Since this
  change involves no code, the outcome of this assignment will be a piece of
  documentation. Here are the requirements for this documentation:
</p>

<ul>
  <li>
    It will be written in <a href="https://www.markdownguide.org/">Markdown</a>
  </li>
  <li>
    Within the repo, it will be located at <code>docs/client-research.md</code>
  </li>
  <li>
    The document must contain the following information:
    <ul>
      <li>What issues did you encounter interacting with the site?</li>
      <li>What issues did you encounter placing an order?</li>
      <li>Describe anything on the site you found really interesting?</li>
      <li>What makes this site unique?</li>
      <li>Links to articles about them and any recurring themes.</li>
      <li>What reviews did you find and any common complaints.</li>
      <li>
        What sites provide similar functionality? How do they compare to the
        existing site?
      </li>
    </ul>
  </li>
</ul>

<p>
  Unlike most assignments, all revisions must be completed by the date of the
  customer meeting. If you submit the Pull Request early, you will most likely
  receive suggestions on further ways to prepare.
</p>
